package training.evaluation.training.rest;

import io.swagger.annotations.Api;
import io.swagger.annotations.ApiOperation;
import io.swagger.annotations.ApiParam;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.*;
import training.evaluation.training.model.TrainingRequest;
import training.evaluation.training.service.ITrainingServices;

@CrossOrigin(origins = {"${origins}"})
@RestController
@RequestMapping(value = "/training")
@Api(description = "Resource to expose all available training endpoints", tags = {"TrainingResource"})
public class TrainingRequestResource {

    @Autowired
    ITrainingServices services;

    @PostMapping("/createTrainingRequest")
    @ApiOperation(value = "Create new training request", notes = "Training request must contain user id and training id. ")
    public TrainingRequest createNewTrainingRequest(@ApiParam(value = "Training Request in JSON format. User id and training id  are required fields, ID is autogenerated. Default status value is PENDING ", required = true) @RequestBody TrainingRequest trainingRequest) {
        trainingRequest.setStatus("PENDING");
        trainingRequest.setCompleted(false);
        return services.createTrainingRequest(trainingRequest);
    }
}
